#include "libft.h"

int		ft_clearspace(const char *s, int c)
{
	if (c == 0)
		while (s[c] == ' ' || s[c] == '\t' || c[s] == '\n')
			c++;
	else
		while (s[c] == ' ' || s[c] == '\t' || s[c] == '\n' || s[c] == '\0')
			c--;
	return (c);
}
