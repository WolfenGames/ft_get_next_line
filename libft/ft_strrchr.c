#include "libft.h"

t_str	ft_strrchr(t_cstr s, int c)
{
	int		i;
	t_str	s1;

	s1 = (t_str)s;
	i = 0;
	while (s1[i])
		i++;
	while (i >= 0)
	{
		if (s1[i] == (char)c)
			return (&s1[i]);
		i--;
	}
	return (NULL);
}
